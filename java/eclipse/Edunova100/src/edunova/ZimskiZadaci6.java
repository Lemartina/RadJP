package edunova;

public class ZimskiZadaci6 {

	public static void main(String[] args) {
		for (int i=14; i<=16; i++) {
			
			System.out.println(i + "-");
						
		}

		
	for (int i=10; i<=13; i++) {
		
			System.out.print(i + "-");
			
			
		}

		
		for (int i=9; i>=6; i--) {	
			System.out.println(i + "-");
		
			
		}
		
		for (int i=5; i>=1; i--) {
					
			System.out.print(i + "-");
		}

	}

}
