package edunova;

public class Racunalo {
	private String marka;
	private boolean graficka;
	private double cijena;
	private char oznakaSerije;
	private int memorija;
	
	
	public String getMarka() {
		return marka;
	}
	public void setMarka(String marka) {
		this.marka = marka;
	}
	public boolean isGraficka() {
		return graficka;
	}
	public void setGraficka(boolean graficka) {
		this.graficka = graficka;
	}
	public double getCijena() {
		return cijena;
	}
	public void setCijena(double cijena) {
		this.cijena = cijena;
	}
	public char getOznakaSerije() {
		return oznakaSerije;
	}
	public void setOznakaSerije(char oznakaSerije) {
		this.oznakaSerije = oznakaSerije;
	}
	public int getMemorija() {
		return memorija;
	}
	public void setMemorija(int memorija) {
		this.memorija = memorija;
	}
	
	

}
